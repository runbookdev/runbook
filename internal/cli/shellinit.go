func newShellInitCmd() *cobra.Command {
	var shellFlag string

	cmd := &cobra.Command{
		Use:   "shell-init",
		Short: "Output shell integration snippet (source in your profile)",
		Long: `Output a shell snippet that installs runbook integration.

The snippet provides:
  • Tab completion for all commands, flags, and .runbook files
  • alias rb="runbook"
  • runbook-detect — scans the current directory and reports the project environment

Add to your shell profile:

  Bash (~/.bashrc):
    eval "$(runbook shell-init)"

  Zsh (~/.zshrc):
    eval "$(runbook shell-init)"

  Fish (~/.config/fish/config.fish):
    runbook shell-init --shell fish | source`,
		RunE: func(cmd *cobra.Command, args []string) error {
			shell := shellFlag
			if shell == "" {
				shell = detectShell()
			}
			switch shell {
			case "bash":
				return writePosixShellInit(os.Stdout, cmd.Root(), "bash")
			case "zsh":
				return writePosixShellInit(os.Stdout, cmd.Root(), "zsh")
			case "fish":
				return writeFishShellInit(os.Stdout, cmd.Root())
			default:
				return fmt.Errorf("unsupported shell %q; supported shells: bash, zsh, fish", shell)
			}
		},
	}

	cmd.Flags().StringVar(&shellFlag, "shell", "", "target shell (bash, zsh, fish); defaults to $SHELL basename")
	_ = cmd.RegisterFlagCompletionFunc("shell", func(_ *cobra.Command, _ []string, _ string) ([]string, cobra.ShellCompDirective) {
		return []string{"bash", "zsh", "fish"}, cobra.ShellCompDirectiveNoFileComp
	})

	return cmd
}

// detectShell returns the base name of $SHELL (e.g. "bash", "zsh", "fish").
func detectShell() string {
	return filepath.Base(os.Getenv("SHELL"))
}

// shellWriter wraps an io.Writer and accumulates the first write error,
// so callers can chain writes and check the error once at the end.
type shellWriter struct {
	w   io.Writer
	err error
}

func (sw *shellWriter) s(str string) {
	if sw.err == nil {
		_, sw.err = io.WriteString(sw.w, str)
	}
}

func (sw *shellWriter) f(format string, args ...any) {
	if sw.err == nil {
		_, sw.err = fmt.Fprintf(sw.w, format, args...)
	}
}

// writePosixShellInit writes the bash or zsh integration snippet to w.
func writePosixShellInit(w io.Writer, root *cobra.Command, shell string) error {
	var compBuf bytes.Buffer
	switch shell {
	case "bash":
		if err := root.GenBashCompletionV2(&compBuf, true); err != nil {
			return fmt.Errorf("generating completion: %w", err)
		}
	case "zsh":
		if err := root.GenZshCompletion(&compBuf); err != nil {
			return fmt.Errorf("generating completion: %w", err)
		}
	}

	promptVar := "PS1"
	if shell == "zsh" {
		promptVar = "PROMPT"
	}

	sw := &shellWriter{w: w}

	// Header
	sw.f("# ── runbook shell integration (%s) %s\n", shell, divider(50))
	sw.f("# Generated by: runbook shell-init --shell %s\n", shell)
	sw.s("# Source in your profile:  eval \"$(runbook shell-init)\"\n\n")

	// Inlined completion script — no subprocess at shell startup.
	sw.f("# ── Tab completion (inlined) %s\n", divider(52))
	sw.s(compBuf.String())
	sw.s("\n")

	// Alias
	sw.f("# ── Alias %s\n", divider(60))
	sw.s("alias rb=\"runbook\"\n\n")

	// runbook-detect function
	sw.f("# ── runbook-detect: project environment summary %s\n", divider(33))
	sw.s(posixDetectFunc)
	sw.s("\n")

	// Prompt indicator + optional integration
	sw.f("# ── Prompt integration (optional) %s\n", divider(46))
	sw.s(posixPromptIndicatorFunc)
	sw.f("# Uncomment to prepend a runbook indicator to your %s:\n", promptVar)
	sw.f("# export %s='$(runbook-prompt-indicator)'\"%s\"\n\n", promptVar, "$"+promptVar)

	return sw.err
}

// writeFishShellInit writes the fish integration snippet to w.
func writeFishShellInit(w io.Writer, root *cobra.Command) error {
	var compBuf bytes.Buffer
	if err := root.GenFishCompletion(&compBuf, true); err != nil {
		return fmt.Errorf("generating completion: %w", err)
	}

	sw := &shellWriter{w: w}

	// Header
	sw.s("# ── runbook shell integration (fish) " + divider(43) + "\n")
	sw.s("# Generated by: runbook shell-init --shell fish\n")
	sw.s("# Source in ~/.config/fish/config.fish:\n")
	sw.s("#   runbook shell-init --shell fish | source\n\n")

	// Inlined completion script
	sw.s("# ── Tab completion (inlined) " + divider(52) + "\n")
	sw.s(compBuf.String())
	sw.s("\n")

	// Alias (fish uses abbr for interactive abbreviations)
	sw.s("# ── Alias " + divider(60) + "\n")
	sw.s("abbr --add rb runbook\n\n")

	// runbook-detect function
	sw.s("# ── runbook-detect: project environment summary " + divider(33) + "\n")
	sw.s(fishDetectFunc)
	sw.s("\n")

	// Prompt indicator + optional integration
	sw.s("# ── Prompt integration (optional) " + divider(46) + "\n")
	sw.s(fishPromptIndicatorFunc)
	sw.s("# Uncomment to prepend a runbook indicator to your fish prompt:\n")
	sw.s("# functions --copy fish_prompt __runbook_orig_fish_prompt 2>/dev/null\n")
	sw.s("# if not functions --query __runbook_orig_fish_prompt\n")
	sw.s("#   function __runbook_orig_fish_prompt; end\n")
	sw.s("# end\n")
	sw.s("# function fish_prompt\n")
	sw.s("#   echo -n (runbook-prompt-indicator)\n")
	sw.s("#   __runbook_orig_fish_prompt\n")
	sw.s("# end\n\n")

	return sw.err
}

// divider returns a string of "─" characters of length n.
func divider(n int) string {
	b := make([]byte, 0, n*3) // ─ is 3 bytes in UTF-8
	for range n {
		b = append(b, "─"...)
	}
	return string(b)
}

