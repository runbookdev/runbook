// posixDetectFunc is the runbook-detect shell function for bash and zsh.
// Variable names use a short _rb_ prefix to avoid collisions with user env.
// Process substitution (<(...)) is used for line-by-line awk output; this
// requires bash or zsh and will not work in plain /bin/sh.
const posixDetectFunc = `runbook-detect() {
  local _d="${1:-.}"
  local _type="unknown" _tools="" _name
  local _count=0 _names="" _envs="" _rbtools="" _all="" _stat=""
  local _f _e _t

  # ── Project type detection ───────────────────────────────────────────────
  if   [ -f "$_d/go.mod" ]; then
    _type="Go project"; _tools="go golangci-lint"
  elif [ -f "$_d/package.json" ]; then
    _type="Node.js project"
    if   [ -f "$_d/pnpm-lock.yaml" ]; then _tools="node pnpm"
    elif [ -f "$_d/yarn.lock" ];       then _tools="node yarn"
    else                                    _tools="node npm"
    fi
  elif [ -f "$_d/Cargo.toml" ]; then
    _type="Rust project"; _tools="cargo rustc"
  elif [ -f "$_d/pyproject.toml" ] || [ -f "$_d/requirements.txt" ]; then
    _type="Python project"; _tools="python3 pip"
  elif [ -f "$_d/Dockerfile" ]; then
    _type="Docker project"; _tools="docker"
  elif [ -f "$_d/docker-compose.yml" ] || [ -f "$_d/docker-compose.yaml" ]; then
    _type="Docker Compose"; _tools="docker"
  elif [ -f "$_d/Makefile" ]; then
    _type="Make-based build"; _tools="make"
  elif ls "$_d"/*.tf >/dev/null 2>&1; then
    _type="Terraform project"; _tools="terraform"
  elif [ -d "$_d/kubernetes" ] || [ -d "$_d/k8s" ]; then
    _type="Kubernetes project"; _tools="kubectl helm"
  elif [ -d "$_d/.github/workflows" ]; then
    _type="GitHub Actions CI"; _tools="gh"
  elif [ -f "$_d/Jenkinsfile" ]; then
    _type="Jenkins CI"
  fi

  _name=$(basename "$(cd "$_d" && pwd)")

  # ── Scan .runbook files, extract environments and required tools ──────────
  if ls "$_d"/*.runbook >/dev/null 2>&1; then
    for _f in "$_d"/*.runbook; do
      _count=$((_count + 1))
      _names="${_names:+$_names, }$(basename "$_f")"

      while IFS= read -r _e; do
        [ -z "$_e" ] && continue
        case " $_envs " in
          *" $_e "*) ;;
          *) _envs="${_envs:+$_envs, }$_e" ;;
        esac
      done < <(awk 'NR==1&&/^---/{f=1;next} f&&/^---/{exit} f&&/^environments:/{if(/\[/){s=$0;sub(/^environments:[[:space:]]*\[[[:space:]]*/,"",s);sub(/[[:space:]]*\].*/,"",s);n=split(s,a,/,[[:space:]]*/);for(i=1;i<=n;i++)if(length(a[i]))print a[i]}else{e=1};next} e&&/^  - /{print substr($0,5);next} e&&/^[^[:blank:]]/{e=0}' "$_f" 2>/dev/null)

      while IFS= read -r _t; do
        [ -z "$_t" ] && continue
        case " $_rbtools " in
          *" $_t "*) ;;
          *) _rbtools="${_rbtools:+$_rbtools }$_t" ;;
        esac
      done < <(awk 'NR==1&&/^---/{f=1;next} f&&/^---/{exit} f&&/^requires:/{r=1;next} r&&/^  tools:/{if(/\[/){s=$0;sub(/^  tools:[[:space:]]*\[[[:space:]]*/,"",s);sub(/[[:space:]]*\].*/,"",s);n=split(s,a,/,[[:space:]]*/);for(i=1;i<=n;i++)if(length(a[i]))print a[i]}else{t=1};r=0;next} r&&/^[^[:blank:]]/{r=0} t&&/^    - /{print substr($0,7);next} t&&/^[^[:blank:]]/{t=0}' "$_f" 2>/dev/null)
    done
  fi

  # ── Merge project tools + runbook-required tools, deduplicate ────────────
  for _t in $_tools $_rbtools; do
    [ -z "$_t" ] && continue
    case " $_all " in
      *" $_t "*) ;;
      *) _all="${_all:+$_all }$_t" ;;
    esac
  done

  # ── Check each tool with command -v ──────────────────────────────────────
  for _t in $_all; do
    [ -z "$_t" ] && continue
    if command -v "$_t" >/dev/null 2>&1; then
      _stat="${_stat:+$_stat, }$_t ✓"
    else
      _stat="${_stat:+$_stat, }$_t ✗"
    fi
  done

  # ── Output summary ───────────────────────────────────────────────────────
  printf '📂 Project: %s (%s)\n' "$_name" "$_type"
  if [ "$_count" -gt 0 ]; then
    printf '📋 Runbooks: %d found (%s)\n' "$_count" "$_names"
  else
    printf '📋 Runbooks: none found\n'
  fi
  [ -n "$_stat" ] && printf '🔧 Tools: %s\n' "$_stat"
  [ -n "$_envs" ] && printf '🌍 Environments: %s\n' "$_envs"
  return 0
}
`

// posixPromptIndicatorFunc is the runbook-prompt-indicator function for bash/zsh.
// It is intentionally minimal: a fast glob count with no parsing.
const posixPromptIndicatorFunc = `runbook-prompt-indicator() {
  local _n=0 _f
  for _f in *.runbook; do [ -f "$_f" ] && _n=$((_n + 1)); done
  [ "$_n" -gt 0 ] && printf '[rb:%d] ' "$_n"
}
`

// fishDetectFunc is the runbook-detect function for fish shell.
const fishDetectFunc = `function runbook-detect
    set -l _d .
    test (count $argv) -gt 0; and set _d $argv[1]

    set -l _type "unknown"
    set -l _tools

    # ── Project type detection ─────────────────────────────────────────────
    if test -f $_d/go.mod
        set _type "Go project"; set _tools go golangci-lint
    else if test -f $_d/package.json
        set _type "Node.js project"
        if test -f $_d/pnpm-lock.yaml;   set _tools node pnpm
        else if test -f $_d/yarn.lock;   set _tools node yarn
        else;                            set _tools node npm
        end
    else if test -f $_d/Cargo.toml
        set _type "Rust project"; set _tools cargo rustc
    else if test -f $_d/pyproject.toml; or test -f $_d/requirements.txt
        set _type "Python project"; set _tools python3 pip
    else if test -f $_d/Dockerfile
        set _type "Docker project"; set _tools docker
    else if test -f $_d/docker-compose.yml; or test -f $_d/docker-compose.yaml
        set _type "Docker Compose"; set _tools docker
    else if test -f $_d/Makefile
        set _type "Make-based build"; set _tools make
    else if test -n (find $_d -maxdepth 1 -name '*.tf' -print -quit 2>/dev/null)
        set _type "Terraform project"; set _tools terraform
    else if test -d $_d/kubernetes; or test -d $_d/k8s
        set _type "Kubernetes project"; set _tools kubectl helm
    else if test -d $_d/.github/workflows
        set _type "GitHub Actions CI"; set _tools gh
    else if test -f $_d/Jenkinsfile
        set _type "Jenkins CI"
    end

    set -l _name
    if test "$_d" = .
        set _name (basename (pwd))
    else
        set _name (basename $_d)
    end

    # ── Scan .runbook files ────────────────────────────────────────────────
    set -l _count 0
    set -l _names
    set -l _envs
    set -l _rbtools

    for _f in $_d/*.runbook
        test -f $_f; or continue
        set _count (math $_count + 1)
        set -a _names (basename $_f)

        for _e in (awk 'NR==1&&/^---/{f=1;next} f&&/^---/{exit} f&&/^environments:/{if(/\[/){s=$0;sub(/^environments:[[:space:]]*\[[[:space:]]*/,"",s);sub(/[[:space:]]*\].*/,"",s);n=split(s,a,/,[[:space:]]*/);for(i=1;i<=n;i++)if(length(a[i]))print a[i]}else{e=1};next} e&&/^  - /{print substr($0,5);next} e&&/^[^[:blank:]]/{e=0}' $_f 2>/dev/null)
            contains -- $_e $_envs; or set -a _envs $_e
        end

        for _t in (awk 'NR==1&&/^---/{f=1;next} f&&/^---/{exit} f&&/^requires:/{r=1;next} r&&/^  tools:/{if(/\[/){s=$0;sub(/^  tools:[[:space:]]*\[[[:space:]]*/,"",s);sub(/[[:space:]]*\].*/,"",s);n=split(s,a,/,[[:space:]]*/);for(i=1;i<=n;i++)if(length(a[i]))print a[i]}else{t=1};r=0;next} r&&/^[^[:blank:]]/{r=0} t&&/^    - /{print substr($0,7);next} t&&/^[^[:blank:]]/{t=0}' $_f 2>/dev/null)
            contains -- $_t $_rbtools; or set -a _rbtools $_t
        end
    end

    # ── Merge and deduplicate tools ────────────────────────────────────────
    set -l _all $_tools
    for _t in $_rbtools
        contains -- $_t $_all; or set -a _all $_t
    end

    # ── Check each tool with type -q ──────────────────────────────────────
    set -l _parts
    for _t in $_all
        if type -q $_t
            set -a _parts "$_t ✓"
        else
            set -a _parts "$_t ✗"
        end
    end

    # ── Output summary ─────────────────────────────────────────────────────
    echo "📂 Project: $_name ($_type)"
    if test $_count -gt 0
        echo "📋 Runbooks: $_count found "(string join ', ' $_names)
    else
        echo "📋 Runbooks: none found"
    end
    test (count $_parts) -gt 0; and echo "🔧 Tools: "(string join ', ' $_parts)
    test (count $_envs)  -gt 0; and echo "🌍 Environments: "(string join ', ' $_envs)
    return 0
end
`

// fishPromptIndicatorFunc is the runbook-prompt-indicator function for fish.
// Fast: glob count only, no file parsing.
const fishPromptIndicatorFunc = `function runbook-prompt-indicator
    set -l _files (ls -1 *.runbook 2>/dev/null)
    set -l _n (count $_files)
    test $_n -gt 0; and echo -n "[rb:$_n] "
end
`
